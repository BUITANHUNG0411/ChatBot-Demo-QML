/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == GPIO_PIN_8) {
    if (sonar_measuring) {
      uint32_t now_tick = DWT->CYCCNT;
      if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8) == GPIO_PIN_SET) {
        sonar_echo_start = now_tick;
      } else {
        uint32_t pulse_us = (now_tick - sonar_echo_start) / 72;
        sonar_distance_cm = (pulse_us * 340.0f) / 2000.0f;
        sonar_measuring = 0;
      }
    }
  }
}
